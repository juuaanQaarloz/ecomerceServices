package com.mx.xpertys.dto;

public class Justificaciones {
	
	private String idAsistencia;
	private String matricula;
	private String nombreAlumno;
	private String indAsistencia;
	private String idProfesor;
	private String fechaAsistencia;
	private String materia;
	private String idJustificacion;
	private String motivo;
	private String autorizado;
	
	public String getIdAsistencia() {
		return idAsistencia;
	}
	public void setIdAsistencia(String idAsistencia) {
		this.idAsistencia = idAsistencia;
	}
	public String getMatricula() {
		return matricula;
	}
	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}
	public String getNombreAlumno() {
		return nombreAlumno;
	}
	public void setNombreAlumno(String nombreAlumno) {
		this.nombreAlumno = nombreAlumno;
	}
	public String getIndAsistencia() {
		return indAsistencia;
	}
	public void setIndAsistencia(String indAsistencia) {
		this.indAsistencia = indAsistencia;
	}
	public String getIdProfesor() {
		return idProfesor;
	}
	public void setIdProfesor(String idProfesor) {
		this.idProfesor = idProfesor;
	}
	public String getFechaAsistencia() {
		return fechaAsistencia;
	}
	public void setFechaAsistencia(String fechaAsistencia) {
		this.fechaAsistencia = fechaAsistencia;
	}
	public String getMateria() {
		return materia;
	}
	public void setMateria(String materia) {
		this.materia = materia;
	}
	public String getIdJustificacion() {
		return idJustificacion;
	}
	public void setIdJustificacion(String idJustificacion) {
		this.idJustificacion = idJustificacion;
	}
	public String getMotivo() {
		return motivo;
	}
	public void setMotivo(String motivo) {
		this.motivo = motivo;
	}
	public String getAutorizado() {
		return autorizado;
	}
	public void setAutorizado(String autorizado) {
		this.autorizado = autorizado;
	}
	
}
