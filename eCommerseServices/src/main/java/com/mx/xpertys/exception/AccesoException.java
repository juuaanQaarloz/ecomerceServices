package com.mx.xpertys.exception;

public class AccesoException extends RuntimeException {
	
	public AccesoException() {
		super();
	}

	public AccesoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AccesoException(String arg0) {
		super(arg0);
	}

	public AccesoException(Throwable arg0) {
		super(arg0);
	}

	

	

}
